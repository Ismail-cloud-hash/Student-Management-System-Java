    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
         try {
        Connection con = DBConnection.connect();
        String sql = "INSERT INTO payment_details (student_id, name, course, course_fee, amount_paid, remaining_balance, payment_date, payment_month, payment_method, payment_status) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, txtstudentId.getText());
        pst.setString(2, txtStudentName.getText());
        pst.setString(3, txtCourseName.getText());
        pst.setDouble(4, Double.parseDouble(txtTotalFee.getText()));
        pst.setDouble(5, Double.parseDouble(txtAmountPaid.getText()));
        pst.setDouble(6, Double.parseDouble(txtRemaining.getText()));

        java.util.Date date = dateChooser.getDate();
        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        pst.setDate(7, sqlDate);

        pst.setString(8, comboMonth.getSelectedItem().toString());
        pst.setString(9, comboMethod.getSelectedItem().toString());
        pst.setString(10, comboStatus.getSelectedItem().toString());

        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Payment saved successfully!");

        con.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
        
       
    }//GEN-LAST:event_btnSubmitActionPerformed

