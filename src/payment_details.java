    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String studentId = txtstudentId.getText();
    try {
        Connection con = DBConnection.connect();

        // ✅ Updated SQL to include photo_path
        String sql = "SELECT name, course, course_fee, photo_path FROM student_details WHERE student_id = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, studentId);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            // ✅ Set student data to fields
            txtStudentName.setText(rs.getString("name"));
            txtCourseName.setText(rs.getString("course"));
            txtTotalFee.setText(rs.getString("course_fee"));

            // ✅ Load and show image
            String imgPath = rs.getString("photo_path");
            if (imgPath != null && !imgPath.isEmpty()) {
                ImageIcon icon = new ImageIcon(imgPath);
                Image image = icon.getImage().getScaledInstance(150, 150, Image.SCALE_SMOOTH);
                paymentPicLabel.setIcon(new ImageIcon(image));
            } else {
                paymentPicLabel.setIcon(null); // Or set default image
            }

        } else {
            JOptionPane.showMessageDialog(null, "❌ Student ID not found!");
            // Clear old data
            txtStudentName.setText("");
            txtCourseName.setText("");
            txtTotalFee.setText("");
            paymentPicLabel.setIcon(null);
        }

        con.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "❌ Error: " + e.getMessage());
    }


    }//GEN-LAST:event_btnSearchActionPerformed

