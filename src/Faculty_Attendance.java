    public void loadStudentsByCourse(String course) {
    try {
        PreparedStatement ps = conn.prepareStatement("SELECT student_id, name FROM student_details WHERE course = ?");
        ps.setString(1, course);
        ResultSet rs = ps.executeQuery();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Student ID");
        model.addColumn("Student Name");
        model.addColumn("Status");

        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getString("student_id"),
                rs.getString("name"),
                "Present"  // Default status
            });
        }

        attendanceTable.setModel(model);

        // Add dropdown for Present/Absent
        String[] statuses = { "Present", "Absent" };
        TableColumn statusCol = attendanceTable.getColumnModel().getColumn(2);
        statusCol.setCellEditor(new DefaultCellEditor(new JComboBox<>(statuses)));

    } catch (Exception e) {
        e.printStackTrace();
    }
}
    
    
    public void loadCourses() {
    try {
        PreparedStatement ps = conn.prepareStatement("SELECT DISTINCT course FROM student_details");
        ResultSet rs = ps.executeQuery();
        courseCombo.removeAllItems();
        while (rs.next()) {
            courseCombo.addItem(rs.getString("course"));
            
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}
