    private void markAttendanceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_markAttendanceBtnActionPerformed
        // TODO add your handling code here:
        try {
        for (int i = 0; i < attendanceTable.getRowCount(); i++) {
            String studentId = attendanceTable.getValueAt(i, 0).toString();
            String course = courseCombo.getSelectedItem().toString();
            java.sql.Date date = new java.sql.Date(dateChooser.getDate().getTime());
            String status = attendanceTable.getValueAt(i, 2).toString();

            PreparedStatement ps = conn.prepareStatement(
                "INSERT INTO attendance (student_id, course, date, status) VALUES (?, ?, ?, ?)"
            );
            ps.setString(1, studentId);
            ps.setString(2, course);
            ps.setDate(3, date);
            ps.setString(4, status);
            ps.executeUpdate();
        }

        JOptionPane.showMessageDialog(this, "✅ Attendance saved successfully!");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "❌ Error: " + e.getMessage());
    }
        
            
    }//GEN-LAST:event_markAttendanceBtnActionPerformed

