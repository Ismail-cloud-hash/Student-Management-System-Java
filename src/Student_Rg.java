    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
       try {
        String idText = jTextFieldStudentID.getText().trim();
        if (!idText.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "❌ Enter a valid Student ID to search.");
            return;
        }
        int studentId = Integer.parseInt(idText);
        Connection con = DBConnection.connect();
        String sql = "SELECT * FROM student_details WHERE student_id=?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, studentId);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            // Set text fields
            jTextFieldName.setText(rs.getString("name"));
            jTextFieldDOB.setText(rs.getString("dob"));
            jComboBoxGender.setSelectedItem(rs.getString("gender"));
            jTextFieldEmail.setText(rs.getString("email"));
            jTextFieldPhone.setText(rs.getString("phone"));
            jComboBoxDepartment.setSelectedItem(rs.getString("department"));
            jComboBoxCourse.setSelectedItem(rs.getString("course"));
            jTextFieldFee.setText(rs.getString("course_fee"));

            // ✅ Load and display profile picture
            selectedImagePath = rs.getString("photo_path"); // get path from DB
            if (selectedImagePath != null && !selectedImagePath.isEmpty()) {
                ImageIcon icon = new ImageIcon(selectedImagePath);
                Image img = icon.getImage().getScaledInstance(150, 150, Image.SCALE_SMOOTH);
                profilePicLabel.setIcon(new ImageIcon(img));
            } else {
                profilePicLabel.setIcon(null); // or show default image
            }

        } else {
            JOptionPane.showMessageDialog(this, "⚠️ No student found with that ID.");
            clearFields();
            profilePicLabel.setIcon(null); // clear image too
        }

        rs.close();
        pst.close();
        con.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "❌ SQL Error: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "❌ Error: " + e.getMessage());
    }
      

    }//GEN-LAST:event_jButton4ActionPerformed

