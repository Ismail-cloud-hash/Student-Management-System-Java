    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
        Connection con = DBConnection.connect();

        // Validate Student ID
        String idText = jTextFieldStudentID.getText().trim();
        if (!idText.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "❌ Enter a valid Student ID to update.");
            return;
        }
        int studentId = Integer.parseInt(idText);

        // Name
        String name = jTextFieldName.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "❌ Name cannot be empty.");
            return;
        }

        // DOB
        String dobText = jTextFieldDOB.getText().trim();
        java.sql.Date dob;
        try {
            dob = java.sql.Date.valueOf(dobText);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, "❌ Invalid DOB format. Use yyyy-MM-dd.");
            return;
        }

        // Gender
        if (jComboBoxGender.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "❌ Select Gender.");
            return;
        }
        String gender = jComboBoxGender.getSelectedItem().toString();

        // Email
        String email = jTextFieldEmail.getText().trim();
        if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            JOptionPane.showMessageDialog(this, "❌ Invalid email.");
            return;
        }

        // Phone
        String phone = jTextFieldPhone.getText().trim();
        if (!phone.matches("\\d{10,15}")) {
            JOptionPane.showMessageDialog(this, "❌ Phone must be 10–15 digits.");
            return;
        }

        // Department
        if (jComboBoxDepartment.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "❌ Select Department.");
            return;
        }
        String department = jComboBoxDepartment.getSelectedItem().toString();

        // Course
        if (jComboBoxCourse.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "❌ Select Course.");
            return;
        }
        String course = jComboBoxCourse.getSelectedItem().toString();

        // Fee
        String feeText = jTextFieldFee.getText().trim();
        java.math.BigDecimal fee;
        try {
            fee = new java.math.BigDecimal(feeText);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "❌ Invalid fee format.");
            return;
        }

        // SQL Update
        String sql = "UPDATE student_details SET name=?, dob=?, gender=?, email=?, phone=?, department=?, course=?, course_fee=? WHERE student_id=?";
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, name);
        pst.setDate(2, dob);
        pst.setString(3, gender);
        pst.setString(4, email);
        pst.setString(5, phone);
        pst.setString(6, department);
        pst.setString(7, course);
        pst.setBigDecimal(8, fee);
        pst.setInt(9, studentId);

        int rowsUpdated = pst.executeUpdate();

        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "✅ Student record updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "⚠️ No student found with this ID.");
        }

        pst.close();
        con.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "❌ SQL Error: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "❌ Error: " + e.getMessage());
    }   
    }//GEN-LAST:event_jButton2ActionPerformed

