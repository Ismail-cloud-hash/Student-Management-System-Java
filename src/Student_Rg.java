    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        String idText = jTextFieldStudentID.getText().trim();

        // Validate Student ID
        if (!idText.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "❌ Enter a valid numeric Student ID to delete.");
            return;
        }

        int studentId = Integer.parseInt(idText);

        // Confirm before delete
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete the student with ID: " + studentId + "?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        // DB Connection
        Connection con = DBConnection.connect();
        String sql = "DELETE FROM student_details WHERE student_id=?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, studentId);

        int rowsDeleted = pst.executeUpdate();

        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "✅ Student deleted successfully!");
            clearFields(); // Optional: clear the form after delete
        } else {
            JOptionPane.showMessageDialog(this, "⚠️ No student found with that ID.");
        }

        pst.close();
        con.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "❌ SQL Error: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "❌ Error: " + e.getMessage());
    }
  
        
    }//GEN-LAST:event_jButton1ActionPerformed

