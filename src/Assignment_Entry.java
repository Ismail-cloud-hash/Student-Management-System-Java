    private void submitAssignmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitAssignmentActionPerformed
        // TODO add your handling code here:
        String studentId = (String) studentCombo.getSelectedItem();
        if (selectedFile == null) {
        JOptionPane.showMessageDialog(this, "Please select a file.");
        return;
    }
    String assignmentName = selectedFile.getName(); // Use file name as assignment name
        String status = (String) statusCombo.getSelectedItem();
        java.util.Date utilDate = submissionDateChooser.getDate();
        if (studentId.isEmpty() || assignmentName.isEmpty() || status.isEmpty() || utilDate == null) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }
        java.sql.Date submissionDate = new java.sql.Date(utilDate.getTime());

        try (Connection conn = DBConnection.connect()) {
            PreparedStatement ps = conn.prepareStatement(
            "INSERT INTO assignments (student_id, assignment_name, status, submission_date, file_path) VALUES (?, ?, ?, ?, ?)");
            ps.setString(1, studentId);
            ps.setString(2, assignmentName);
            ps.setString(3, status);
            ps.setDate(4, submissionDate);
            ps.setString(5, selectedFile.getAbsolutePath());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "✅ Assignment saved successfully!");
            loadAssignments();
            resetForm();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "❌ Error saving assignment.");
        }
    }//GEN-LAST:event_submitAssignmentActionPerformed

